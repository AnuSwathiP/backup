package httpBasicRequest;

public class PojoData_4 {

	//Encapsulation=data+function
	private String name;
	private String job;
	//rightclick->source->generate getters and setters->check all checkboxes->generate
	//Methods
	public String getName() {
		return name;
	}
	//local data assigned to instance variable
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	
	
}
